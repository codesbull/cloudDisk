        <li>&nbsp;&nbsp;8.在对应文件或文件夹前打勾，然后点击“重命名”按钮，弹出对话框，输入新命名，点击确定即可完成重命名，需要注意的是：重命名暂只支持一次对一个文件或文件夹对象进行重命名操作，勾选多个对象并进行此操作会被认定为失败处理。</li>
