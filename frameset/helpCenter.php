     	<li>&nbsp;&nbsp;4.目前本系统支持的文档文件有("doc","txt","docx","rtf","html","htm","pdf","rar","zip","wri","z","css")，视频文件有("avi","rmvb","rm","asf","divx","mpg","mpeg","mpe","wmv","mp4","mkv","vob","mov","flv","qt")，音频文件有("cd","ogg","mp3","asf","wma","wav","mp3pro","rm","real","module","midi","vqf")，任何其他文件暂不支持，强行上传可能不被检索或不能被正常显示。</li>
